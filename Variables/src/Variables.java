public class Variables {
    public static void main(String[] args) {

        System.out.println("Tipo de dato primitivo: byte");
        byte edad = 30;
        System.out.println("Edad: " + edad);

        System.out.println("Tipo de dato primitivo: short");
        short temperatura = -15;
        System.out.println("Temperatura: " + temperatura);

        System.out.println("Tipo de dato primitivo: int");
        int cantidadEstudiantes = 1000;
        System.out.println("Cantidad de estudiantes: " + cantidadEstudiantes);

        System.out.println("Tipo de dato primitivo: long");
        long poblacionMundial = 7800000000L; // Nota: se usa la letra 'L' al final para indicar que es de tipo long
        System.out.println("Población mundial: " + poblacionMundial);

        System.out.println("Tipo de dato primitivo: float");
        float precio = 45.75f; // Nota: se usa la letra 'f' al final para indicar que es de tipo float
        System.out.println("Precio: " + precio);

        System.out.println("Tipo de dato primitivo: double");
        double altura = 1.85;
        System.out.println("Altura: " + altura);

        System.out.println("Tipo de dato primitivo: char");
        char inicial = 'J';
        System.out.println("Inicial: " + inicial);

        System.out.println("Tipo de dato primitivo: boolean");
        boolean esEstudiante = true;
        System.out.println("¿Es estudiante?: " + esEstudiante);
    }
}
