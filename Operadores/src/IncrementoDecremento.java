public class IncrementoDecremento {
    public static void main(String[] args) {
        System.out.println("Operadores de Incremento/Decremento");
        System.out.println("Estos operadores se utilizan para incrementar o decrementar el valor de una variable.");

        System.out.println("Incremento (++x, x++): Aumenta el valor de x en 1.");

        int x = 5;
        x++;  // x ser치 6
        System.out.println("x = 5: x ++: x ser치  " + x);

        System.out.println("Decremento (--x, x--): Disminuye el valor de x en 1.");

        int y = 5;
        y--;  // x ser치 4
        System.out.println("y = 5: y ser치  " + y);

    }
}
